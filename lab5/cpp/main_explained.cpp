// Додаткові пояснення:
//
// 1. Чому #pragma?
//    #pragma - директива препроцесора, специфічна для компілятора
//    omp - простір імен OpenMP
//    Компілятор без OpenMP просто ігнорує ці рядки!
//
// 2. Що таке reduction(+:sum)?
//    Приклад: 4 потоки, кожен підсумував свою частину:
//    Потік 0: sum = 100
//    Потік 1: sum = 150  
//    Потік 2: sum = 120
//    Потік 3: sum = 80
