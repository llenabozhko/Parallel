// int main() - ГОЛОВНА функція програми
// int = повертає ціле число (0 = успіх, інше = помилка)
// main = обов'язкова назва точки входу в програму
// () = без параметрів
int main() {
    
    // omp_set_nested(1);
    // omp_set_nested() - дозволити ВКЛАДЕНИЙ паралелізм
    // nested = вкладений (паралелізм в паралелізмі)
    // 1 = true (увімкнено), 0 = false (вимкнено)
    // Дозволяє створювати паралельні області всередині паралельних областей
    // Наприклад: parallel sections → parallel for
    omp_set_nested(1);
    
    // init();
    // Виклик функції init() - заповнити масив випадковими числами
    // Функція виконується ПОСЛІДОВНО (один потік)
    init();
    
    // long long total = 0;
    // total = загальна сума всіх елементів
    // 0 = початкове значення
    long long total = 0;
    
    // tuple<int, int> min;
    // min = результат функції min_row_sum
    // tuple<int, int> = кортеж (сума, індекс)
    // ⚠️ Зверни увагу: тут ПОМИЛКА! Має бути tuple<long long, int>!
    //     (сума може бути великою, треба long long)
    tuple<int, int> min;
    
    // double start = omp_get_wtime();
    // double = число з плаваючою комою (8 байт, дуже точне)
    // start = час ПОЧАТКУ вимірювання
    // omp_get_wtime() = Wall-clock Time (час настінного годинника)
    //                   повертає час у секундах з високою точністю
    //                   (можна вимірювати мікросекунди)
    double start = omp_get_wtime();
    
    // ----------------------------------------------------------------------------
    // #pragma omp parallel sections
    // Це ДИРЕКТИВА компілятора OpenMP (починається з #pragma)
    // parallel = створити паралельну область (кілька потоків)
    // sections = розділити код на СЕКЦІЇ, кожна секція - окремий потік
    // {
    //   #pragma omp section  ← секція 1 (потік 1)
    //   #pragma omp section  ← секція 2 (потік 2)
    // }
    // Виконується ПАРАЛЕЛЬНО (одночасно на різних ядрах CPU)
    // ----------------------------------------------------------------------------
    #pragma omp parallel sections
    {
        // #pragma omp section
        // Початок СЕКЦІЇ 1 - виконується одним потоком
        // total = all_rows_total_sum(...) - обчислити загальну суму
        #pragma omp section
        total = all_rows_total_sum(amount_threads);
        
        // #pragma omp section  
        // Початок СЕКЦІЇ 2 - виконується ІНШИМ потоком паралельно з першим!
        // min = min_row_sum(...) - знайти мінімальний рядок
        #pragma omp section
        min = min_row_sum(amount_threads);
    }
    // } - кінець parallel sections
    // Тут відбувається БАР'ЄРНА синхронізація:
    // всі потоки чекають один одного перед продовженням
    
    // double end = omp_get_wtime();
    // end = час ЗАКІНЧЕННЯ вимірювання
    // end - start = загальний час виконання parallel sections
    double end = omp_get_wtime();
    
    // cout << "\n[✓]\tsum:\t" << total << endl;
    // cout = Character Output (вивід символів на екран)
    // << = оператор "вставки" (відправити в cout)
    // "\n" = перенесення на новий рядок (newline)
    // "[✓]\tsum:\t" = текст з табуляцією для вирівнювання
    // ✓ = галочка (успіх)
    // \t = tab (табуляція)
    // total = значення суми
    // endl = End Line (кінець рядка + скидання буфера)
    cout << "\n[✓]\tsum:\t" << total << endl;
    
    // cout << "[✓] min sum:\t" << get<0>(min) << " [idx: " << get<1>(min) << "]\n";
    // get<0>(min) = дістати ПЕРШЕ значення з кортежу (мінімальна сума)
    // get<1>(min) = дістати ДРУГЕ значення з кортежу (індекс рядка)
    // "[idx: " << ... << "]" = красивий вивід: [idx: 42]
    // \n = перенесення рядка
    cout << "[✓] min sum:\t" << get<0>(min) << " [idx: " << get<1>(min) << "]\n";
    
    // cout << "[i]   total:\t" << end - start << "s" << endl;
    // [i] = info (інформація)
    // end - start = різниця часу = скільки секунд виконувалось
    // "s" = seconds (секунди)
    cout << "[i]   total:\t" << end - start << "s" << endl;
    
    // return 0;
    // Повертаємо 0 = програма завершилась УСПІШНО
    // (0 = ОК, будь-яке інше число = помилка)
    return 0;
}

// ----------------------------------------------------------------------------
// БЛОК 6: ФУНКЦІЯ init() - ініціалізація масиву
// ----------------------------------------------------------------------------

void init() {
    // srand(time(NULL));
    // srand = Set Random Seed (встановити зерно для випадкових чисел)
    // time(NULL) = поточний час у секундах з 1970 року
    //              (завжди різний, тому rand() буде різний кожен запуск)
    // Якщо НЕ викликати srand - rand() завжди дасть одну і ту ж послідовність!
    srand(time(NULL));
    
    // cout << "- Init started";
    // Виводимо повідомлення що ініціалізація почалась
    // БЕЗ endl - курсор залишається в цьому ж рядку
    cout << "- Init started";
    
    // double start = omp_get_wtime();
    // Починаємо вимірювати час ініціалізації
    double start = omp_get_wtime();
    
    // #pragma omp parallel for num_threads(amount_threads)
    // parallel for = розпаралелити ЦИКЛ for
    // Кожен потік отримує частину ітерацій
    // Наприклад: 1000 рядків / 8 потоків = 125 рядків на потік
    // num_threads(8) = використовувати РІВНО 8 потоків
    #pragma omp parallel for num_threads(amount_threads)
    
    // for (int i = 0; i < amount_rows; ++i)
    // Цикл по РЯДКАХ масиву
    // int i = 0 - лічильник, починаємо з 0
    // i < amount_rows - умова продовження (поки i < 1000)
    // ++i - збільшити i на 1 після кожної ітерації
    for (int i = 0; i < amount_rows; ++i) {
        
        // for (int j = 0; j < amount_cols; ++j)
        // ВНУТРІШНІЙ цикл по СТОВПЦЯХ
        // j = column index (індекс стовпця)
        for (int j = 0; j < amount_cols; ++j) {
            
            // arr[i][j] = rand();
            // arr[i][j] = елемент масиву на перетині рядка i і стовпця j
            // rand() = генерація випадкового числа (0 до 32767)
            // Присвоюємо випадкове число елементу масиву
            arr[i][j] = rand();
        }
        // Кінець внутрішнього циклу - перейшли всі стовпці рядка i
    }
    // Кінець зовнішнього циклу - перейшли всі рядки
    // parallel for автоматично чекає завершення всіх потоків (бар'єр)
    
    // double end = omp_get_wtime();
    // Закінчуємо вимірювання часу
    double end = omp_get_wtime();
    
    // cout << " and finished in " << end - start << "s\n" << endl;
    // Виводимо час виконання
    // "s\n" = seconds + newline
    // << endl = ще один перенос + скидання буфера
    cout << " and finished in " << end - start << "s\n" << endl;
}

// ----------------------------------------------------------------------------
// БЛОК 7: ФУНКЦІЯ all_rows_total_sum() - сума всіх елементів
// ----------------------------------------------------------------------------

// long long all_rows_total_sum(int amount_threads) {
// Повертає: загальну суму (long long)
// Параметр: кількість потоків
long long all_rows_total_sum(int amount_threads) {
    
    // long long sum = 0;
    // sum = акумулятор для накопичення суми
    // long long = потрібно бо сума 1,000,000 чисел може бути великою!
    long long sum = 0;
    
    // double start = omp_get_wtime();
    // Вимірюємо час виконання цієї функції
    double start = omp_get_wtime();
    
    // #pragma omp parallel for reduction(+:sum) num_threads(amount_threads)
    // parallel for = розпаралелити цикл
    // reduction(+:sum) = ОПЕРАЦІЯ РЕДУКЦІЇ - ключова концепція!!!
    //                    Кожен потік отримує СВОЮ КОПІЮ sum
    //                    Після циклу всі копії додаються в одну загальну sum
    //                    Це безпечний спосіб підсумовування без гонки даних!
    // + = операція додавання
    // sum = змінна для редукції
    #pragma omp parallel for reduction(+:sum) num_threads(amount_threads)
    
    // for (int i = 0; i < amount_rows; ++i)
    // Цикл по рядках - кожен потік обробляє свої рядки
    for (int i = 0; i < amount_rows; ++i) {
        
        // for (int j = 0; j < amount_cols; ++j)
        // Внутрішній цикл по стовпцях (послідовний, не розпаралелений)
        for (int j = 0; j < amount_cols; ++j) {
            
            // sum += arr[i][j];
            // += = додати і присвоїти (sum = sum + arr[i][j])
            // Додаємо поточний елемент до локальної копії sum
            sum += arr[i][j];
        }
    }
    // Після циклу OpenMP автоматично додає всі локальні суми в одну загальну
    
    // double end = omp_get_wtime();
    double end = omp_get_wtime();
    
    // cout << "[i]\tsum:\t"<< sum << " [" << end - start << "s] " << amount_threads << " threads.\n";
    // Виводимо результат і час виконання
    // "\t" = табуляція для вирівнювання колонок
    cout << "[i]\tsum:\t"<< sum << " [" << end - start << "s] " << amount_threads << " threads.\n";
    
    // return sum;
    // Повертаємо загальну суму в функцію main
    return sum;
}

// ----------------------------------------------------------------------------
// БЛОК 8: ФУНКЦІЯ min_row_sum() - пошук рядка з мінімальною сумою
// ----------------------------------------------------------------------------

// tuple<long long, int> min_row_sum(int amount_threads)
// Повертає: кортеж (мінімальна сума рядка, індекс цього рядка)
// Параметр: кількість потоків
tuple<long long, int> min_row_sum(int amount_threads) {
    
    // long long sums[amount_rows];
    // sums = масив сум для КОЖНОГО рядка
    // [amount_rows] = 1000 елементів
    // sums[i] = сума елементів i-го рядка
    // Локальна змінна (на стеку) - тому обмежений розмір!
    long long sums[amount_rows];
    
    // double start = omp_get_wtime();
    // Вимірюємо час
    double start = omp_get_wtime();
    
    // #pragma omp parallel for num_threads(amount_threads)
    // Розпаралелюємо обчислення сум по рядках
    // Кожен потік обчислює суми для своїх рядків
    #pragma omp parallel for num_threads(amount_threads)
    
    // for (int i = 0; i < amount_rows; ++i)
    // Цикл по рядках
    for (int i = 0; i < amount_rows; ++i) {
        
        // for (int j = 0; j < amount_cols; ++j)
        // Внутрішній цикл - підсумовуємо всі елементи рядка i
        for (int j = 0; j < amount_cols; ++j) {
            
            // sums[i] += arr[i][j];
            // Додаємо елемент до суми i-го рядка
            // sums[i] - сума конкретного рядка (не плутати з загальною сумою!)
            sums[i] += arr[i][j];
        }
    }
    // Тепер sums[] містить суму кожного рядка
    
    // long long min_sum = sums[0];
    // min_sum = мінімальна сума (поки що беремо першу як початкову)
    long long min_sum = sums[0];
    
    // int min_idx = 0;
    // min_idx = індекс рядка з мінімальною сумою (поки що 0)
    int min_idx = 0;
    
    // #pragma omp parallel for num_threads(amount_threads)
    // Розпаралелюємо пошук мінімуму
    // Кожен потік шукає мінімум у своїй частині масиву sums[]
    #pragma omp parallel for num_threads(amount_threads)
    
    // for (int i = 0; i < amount_rows; ++i)
    // Перебираємо всі суми рядків
    for (int i = 0; i < amount_rows; ++i) {
        
        // #pragma omp critical
        // КРИТИЧНА СЕКЦІЯ - тільки ОДИН потік може виконувати одночасно!
        // Потрібна бо min_sum і min_idx - спільні змінні
        // Без critical - гонка даних (race condition)!
        #pragma omp critical
        {
            // if (sums[i] < min_sum)
            // Якщо сума i-го рядка МЕНША за поточний мінімум
            if (sums[i] < min_sum) {
                
                // min_sum = sums[i];
                // Оновлюємо мінімальну суму
                min_sum = sums[i];
                
                // min_idx = i;
                // Оновлюємо індекс мінімального рядка
                min_idx = i;
            }
        }
        // Кінець critical - наступний потік може входити
    }
    
    // double end = omp_get_wtime();
    double end = omp_get_wtime();
    
    // tuple<long long, int> min = make_tuple(min_sum, min_idx);
    // make_tuple() = створює кортеж з двох значень
    // min = (мінімальна сума, індекс рядка)
    tuple<long long, int> min = make_tuple(min_sum, min_idx);
    
    // cout << "[i] min sum:\t" << get<1>(min) << " [" << end - start << "s] " << amount_threads << " threads.\n";
    // ⚠️ ТУТ ПОМИЛКА В КОДІ! Має бути get<0>(min) - сума, а не get<1>(min) - індекс!
    cout << "[i] min sum:\t" << get<0>(min) << " [" << end - start << "s] " << amount_threads << " threads.\n";
    
    // return min;
    // Повертаємо кортеж (мінімум, індекс)
    return min;
}

